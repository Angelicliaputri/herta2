    // Check if the text is provided
    if (!text) {
        return conn.reply(m.chat, 'Masukkan Namamu', m);
    }
    
    // Send loading message
    conn.reply(m.chat, 'Sedang meramal Kodam kamuðŸ”®...', m);
    
